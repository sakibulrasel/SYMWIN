package com.edisonit.symwin.model;

public class Lifting {

    String ShortName;
    String LiftingQty;

    public Lifting() {

        ShortName = ShortName;
        LiftingQty = LiftingQty;
    }


    public String getShortName() {
        return ShortName;
    }

    public void setShortName(String shortName) {
        ShortName = shortName;
    }

    public String getLiftingQty() {
        return LiftingQty;
    }

    public void setLiftingQty(String liftingQty) {
        LiftingQty = liftingQty;
    }
}
