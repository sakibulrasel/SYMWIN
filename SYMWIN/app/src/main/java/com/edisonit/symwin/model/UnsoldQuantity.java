package com.edisonit.symwin.model;

public class UnsoldQuantity {

    String ShortName;
    String SoldQty;

    public UnsoldQuantity() {
        ShortName = ShortName;
        SoldQty = SoldQty;
    }

    public String getShortName() {
        return ShortName;
    }

    public void setShortName(String shortName) {
        ShortName = shortName;
    }

    public String getSoldQty() {
        return SoldQty;
    }

    public void setSoldQty(String soldQty) {
        SoldQty = soldQty;
    }
}
